                {
                    Text = "Delete",
                    Size = new Size(80, 40),
                    Tag = modName,
                    Font = new Font(Font.FontFamily, Font.Size + 4, FontStyle.Bold),
                    BackColor = Color.Gray,
                    ForeColor = Color.White,
                    FlatStyle = FlatStyle.Flat
                };
                deleteButton.FlatAppearance.BorderSize = 0;
                deleteButton.Click += fileExplorerManager.DeleteMod;  // <--- Your existing event

                // Potential "Settings" button
                Button settingsButton = null;

                // 6) Check if MGSHDFix or recognized textures
                if (fileExplorerManager.IsMGSHDFixMod(modPath))
                {
                    // MGSHDFix mod => HDFix Settings button
                    settingsButton = new Button
                    {
                        Text = "HDFix Settings",
                        Size = new Size(160, 40),
                        Tag = modName,
                        Font = new Font(Font.FontFamily, Font.Size + 4, FontStyle.Bold),
                        BackColor = Color.LightBlue,
                        ForeColor = Color.Black,
                        FlatStyle = FlatStyle.Flat
                    };
                    settingsButton.FlatAppearance.BorderSize = 0;
                    settingsButton.Click += (s, e) =>
                    {
                        string iniPath = Path.Combine(modPath, "MGSHDFix.ini");
                        if (!File.Exists(iniPath))
                        {
                            MessageBox.Show("MGSHDFix.ini not found in mod folder.", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                            return;
                        }
                        MGSHDFixSettingsForm settingsForm = new MGSHDFixSettingsForm(iniPath, this);
                        settingsForm.ShowDialog();
